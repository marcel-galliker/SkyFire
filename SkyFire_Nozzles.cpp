//--- Samba_initLineMapping --------------------------------------------------
SLineMapping	SF_LineMapping[SF_NOZZLE_CNT];
SLineMapping	LineMapping[SF_NOZZLE_CNT];

void SkyFire_initLineMapping(int backwards, int rightToLeft)
{
	memset(SF_LineMapping, 0, sizeof(SF_LineMapping));
	// witch x goes to witch nozzle (reg+bit)
	for (int line = 0; line < SF_NOZZLE_CNT; line++)
	{
		for (int i = 0; i < SF_NOZZLE_CNT; i++)
		{
			if (line == _Nozzles[i].reg*SF_SR_LEN+_Nozzles[i].bit)
			{
				SF_LineMapping[line].x = _Nozzles[i].x;
				SF_LineMapping[line].y = _Nozzles[i].y;
				break;
			}
		}
	}

	//--- combine Samba convertion --------------
	memset(LineMapping, 0, sizeof(LineMapping));
	for (int line = 0; line < SF_NOZZLE_CNT; line++)
	{
		
		LineMapping[line].x = SB_LineMapping[SB_NOZZLE_CNT-line].x + SF_LineMapping[line].x;
		LineMapping[line].y = SB_LineMapping[SB_NOZZLE_CNT-line].y/2 + SF_LineMapping[line].y;
	}
}
