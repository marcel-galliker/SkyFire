SReverseLineMapping		ReverseLineMapping[NOZZLE_CNT];

void Samba_initLineMapping_reverse()
{
	int i, n;
	memset(ReverseLineMapping, 0xff, sizeof(ReverseLineMapping));
	for (i=0; i<SIZEOF(Nozzles); i++)
	{
//		n = (Nozzles[i].bit - 1) * 32 + Nozzles[i].asic;
		n = (64-Nozzles[i].bit) * 32 + Nozzles[i].asic;
		ReverseLineMapping[n].x = Nozzles[i].x;
		ReverseLineMapping[n].y = Nozzles[i].y;
	}
}

