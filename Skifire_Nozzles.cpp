void Samba_initLineMapping(int backwards, int rightToLeft)
{
//	if (LineMapping) return;
	
	LineCnt = NOZZLE_CNT + NOZZLE_OVERLAP;
	
	int size;
	int line;
	SLineMapping *pLine;
	int i, bitOffset;
	int	max=0;

	size = LineCnt * sizeof(SLineMapping);
//	LineMapping = (SLineMapping*)malloc(size);
	LineMapping = _LineMapping;
	memset(LineMapping, 0, size);
	for (line = 0; line < LineCnt; line++)
	{
		for (i = 0; i < SIZEOF(Nozzles); i++)
		{
//			if (Nozzles[i].x == line + NOZZLE_OVERLAP)
			if (Nozzles[i].x==line)
			{
				if (rightToLeft) pLine = &LineMapping[NOZZLE_CNT + NOZZLE_OVERLAP -1 -line];
				else             pLine = &LineMapping[line];
//				bitOffset = (Nozzles[i].bit - 1) * 32 + Nozzles[i].asic; // 32 shift registers, first bit 0 for all, then bit 1 for all....
				bitOffset = (64-Nozzles[i].bit) * 32 + Nozzles[i].asic; // 32 shift registers, first bit 0 for all, then bit 1 for all....
				pLine->byteOffset = (bitOffset >> 3);
				pLine->bitSet	  = 0x01 << (bitOffset & 7);
				if (backwards) pLine->delay = 609-Nozzles[i].y;
				else           pLine->delay = Nozzles[i].y;
				if (pLine->delay>max) max = pLine->delay;
				break;
			}
		}
	}
}

